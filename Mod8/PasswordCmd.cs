using Smod2;
using Smod2.Commands;

namespace VirtualBrightPlayz.SCPSL.Mod8
{
    internal class PasswordCmd : ICommandHandler
    {
        private Plugin plugin;

        public PasswordCmd(Mod8 mod8)
        {
            this.plugin = mod8;
        }

        string ICommandHandler.GetCommandDescription()
        {
            return "Used for the minimap authentication. This password isn't saved, but don't enter you steam password.";
        }

        string ICommandHandler.GetUsage()
        {
            return "pswd <3-5 letter password>";
        }

        string[] ICommandHandler.OnCall(ICommandSender sender, string[] args)
        {
            plugin.Info(sender.GetType().ToString());
            return new string[] { };
        }
    }
}